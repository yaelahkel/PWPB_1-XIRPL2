  <h4>I. Pengenalan</h4>

World Wide Web adalah layanan internet yang paling populer saat ini. Internet mulai dikenal dan digunakan secara luas setelah adanya layanan WWW. WWW adalah halaman-halaman website yang dapat saling terkoneksi satu dengan lainnya (hyperlink) yang membentuk samudra belantara informasi. WWW berjalan dengan protokol HyperText Transfer Protokol (HTTP), dengan menggunakan HTTP dapat membuat user untuk mengakses suatu halaman web melalui protocol TCP/IP menjadi lebih mudah. <br><br>

Halaman Web merupakan file teks murni (plain text) yang berisi sintaks-sintaks HTML yang dapat dibuka/dilihat/diterjemahkan dengan Internet Browser ketika Sintaks HTML mampu memuat konten text, gambar, audio, video, dan animasi. <br><br>

